a = 20
b = 4
 
# Print bitwise AND operation
print("a & b =", a & b)
 
# Print bitwise OR operation
print("a | b =", a | b)
 
# Print bitwise NOT operation
print("~a =", ~a)
 
# print bitwise XOR operation
print("a ^ b =", a ^ b)

# Binary Left Shift
     
print ("a << 1 : ", a<<1)

# Binary Right Shift
  
print ("a >> 1 : ", a>>1)